#include<iostream>

using namespace std;

int main()
{
    //Character is a data that represents one single character;
    char grade = 'X';

    //String is a data that represents one or multiple characters;
    string name = "Faaiz Haikal Hilmi";

    //Integer is a data that represents an integer number;
    int age = 18;

    //Float is a data that represents decimal number with 7 decimals digit precision and occupies 32-bits;
    float fraction = 0.12345678;
    cout << fraction << endl;

    //Double is same like float but it has 15 decimals digit precision and occupies 64-bits;
    double gpa = 0.123456789;
    cout << gpa << endl;
    
    //Boolean is a data that stores True/False value;
    bool isMale = true;

    return 0;
}